							<h2>Characteristics of HK Private Limited Company:</h2>
							
							<h3>Company Name:</h3>
							<ul>
								<li>A&nbsp;company name must be unique to the existing Hong Kong companies&nbsp;in registrar of Hong Kong Company Registry.</li>
								<li>A company can have
									<ul>
										<li>Either an English name or a Chinese name; or</li>
										<li>Both English and Chinese name.</li>
									</ul>
								</li>
							</ul>
							<h3>Shareholder(s) and Director(s):</h3>
							<ul>
								<li>Minimum Number of Shareholders: 1</li>
								<li>Minimum Number of Directors: 1</li>
								<li>No nationality restriction</li>
								<li>Shareholder(s) is allowable to be appointed as Director(s).</li>
								<li>Acceptable for Individual (i.e. natural person) and/or Corporate (i.e. incorporated body) person*</li>
							</ul>
							<blockquote>
								<p><strong>Remark:</strong></p>
								<p>Corporate directors are acceptable for registration of every private limited company in Hong Kong but at least one of them must be natural person according to New Companies Ordinance (the law of company registration in Hong Kong) effective on 3rd March 2014.</p>
								<p>Therefore, every sole-director company whose director must be a natural person director.</p>
							</blockquote>
							<h3>Statutory Company Secretary and Registered Office Address:</h3>
							<ul>
								<li>Appointment of at least one Company Secretary and one Registered Address are required.</li>
								<li>Company Secretary can be either an individual or corporate.
									<ul>
										<li>They must be either a Hong Kong resident or a Hong Kong limited company. *</li>
									</ul>
								</li>
							</ul>
							<blockquote>
								<p><strong>Remark:</strong></p>
								<p>For sole-director company, the director cannot act as the Company Secretary of the company at the same time.</p>
							</blockquote>
							<ul>
								<li>Registered Office Address is an postal address in Hong Kong.
									<ul>
										<li>Neither P.O. box nor non-postal address is acceptable.</li>
									</ul>
								</li>
							</ul>
							<blockquote>
								<p><strong>Suggestion:</strong></p>
								<p>AsiaBC offers “Hong Kong incorporation Services” to act as&nbsp;statutory company secretary of your company and to part your company’s&nbsp;registered office address to AsiaBC’s office with handling.</p>
							</blockquote>
							<h3>Share Capital Requirement:</h3>
							<ul>
								<li>Minimum subscribed share capital is 1 share.</li>
								<li>Currency is not limited.</li>
								<li>No par-value for share(s).</li>
								<li>No government stamp&nbsp;duty are charged&nbsp;on&nbsp;increasing of nominal share capital and allotment of shares (on or after 1st June 2012)</li>
							</ul>
							<blockquote>
								<p><strong>Suggestion:</strong></p>
								<p>Arrangement of share capital may&nbsp;be a complex issues when the new company consists of more than one owners. AsiaBC offers financial consultation to&nbsp;assist clients to set up a company&nbsp;through a simple and reliable share capital arrangement.</p>
							</blockquote>
							<h3>Public Disclosure:</h3>
							<ul>
								<li>Beneficiary Owner(s): No</li>
								<li>Filings Shareholder(s): Yes</li>
								<li>Filings Director(s): Yes</li>
							</ul>
						
