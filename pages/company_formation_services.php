							<h2>Why Hong Kong Company?</h2>
							
							<ul>
								<li>&#8203;<strong>Low tax rate:</strong> only the profits&nbsp;which are sourced in Hong Kong&nbsp;of businesses are taxed at the rate of 16.5%.</li>
								<li><strong>Simple tax system:</strong> zero&nbsp;VAT, GST and all sorts of sales tax. Zero&nbsp;tax on non-Hong Kong businesses</li>
								<li><strong>World-class financial system:</strong> No foreign exchange control. Bank accounts are offered in multi-currency, with complete online banking services</li>
								<li><strong>Best route&nbsp;to China:</strong> CEPA and lower withholding tax for dividend and royalty in China</li>
								<li><strong>Chinese yuan (CNY) business:</strong>&nbsp;Offshore Renminbi (RMB) clearing centre. Paying or receiving Renminbi (RMB) to and from China business partners, conversion of &nbsp;RMB into other currency in your&nbsp;Hong Kong bank accounts</li>
								<li><strong>Ideal for all business:</strong> international financial centre, free trade port, (almost) no restrictions of&nbsp;business natures&#8203;</li>
								<li><strong>Sound legal system:</strong>&nbsp;independent legal system based on common law. Clean government and business environment.</li>
								<li><strong>Well-developed city for business:</strong>&nbsp;world-class infrastructure, abundant supply of high quality work force.</li>
							</ul>
						
