							<h3>Step 1 : Choose a type:</h3>
							<ul>
								<li>Brand-new&nbsp;company, OR</li>
								<li>Shelf&nbsp;company</li>
							</ul>
							<p><strong>Brand-new company</strong>&nbsp;<em>(namely tailor-made company)</em> is&nbsp;the company which will be incorporated from sketch,&nbsp;its&nbsp;composition totally comply with the laws in Hong Kong.</p>
							<p><strong>Shelf company</strong> <em>(namely Off-the-shelf company or Ready-made company)</em> is the company which has been incorporated for the purpose of reselling, it has never been engaged into any business activities (i.e. no bank accounts), its composition totally comply with the laws in Hong Kong.</p>
							<h3>Step 2 : Choose a sub-type:</h3>
							<p>For Brand-new company:</p>
							<ul>
								<li>Online submission (e-registration), OR</li>
								<li>Paper submission (traditional method)</li>
							</ul>
							<p>For Shelf company:</p>
							<ul>
								<li>Fresh company (younger&nbsp;than its 1st anniversary date), OR</li>
								<li>Aged company (older than its 1st anniversary date)</li>
							</ul>
							<blockquote>
								<p><strong>Comparison:</strong></p>
								<p>Brand-new company by online submission (e-registration) can be processed within 1 day including government processing time while incorporation of a brand-new company by paper submission can take 7 working days by government.</p>
								<p>On the other hand, buying shelf company regardless of its “freshness” takes 2 working days due to government processing time.</p>
							</blockquote>
							<h3>Step 3 : Preparation for company registration</h3>
							<ul>
								<li>Company Name:
									<ul>
										<li>Select several preferred company names to go through the company name search for finding a unique company name.</li>
										<li>For buy shelf company, select your preferred company name from the list of existing company (available upon request).</li>
										<li>Name change of company name is allowable, services and government fee applies.</li>
									</ul>
								</li>
								<li>Shareholder(s) and Director(s):
									<ul>
										<li>Full name / Company Name</li>
										<li>Residential Address / Registered office address</li>
										<li>Nationality / Place of incorporation</li>
										<li>HKID or Passport number / Incorporation number</li>
										<li>Shareholdings structure among shareholders.</li>
									</ul>
								</li>
								<li>Company Secretary:
									<ul>
										<li><strong>If AsiaBC is appointed as your company secretary, the information below is not required.&nbsp;</strong></li>
										<li>Full name / Company Name</li>
										<li>HKID or Passport number &nbsp;/ Incorporation number</li>
										<li>Hong Kong Residential Address / Hong Kong registered office address</li>
									</ul>
								</li>
								<li>Registered Office Address:
									<ul>
										<li><strong>If AsiaBC is hired to be your company registered office address, the information below is not required.</strong></li>
										<li>A Hong Kong local address for government registration, administration and communication purpose. (non-P.O.Box address)</li>
									</ul>
								</li>
							</ul>
						
