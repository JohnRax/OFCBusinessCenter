								</li>
								<li>In case of change of information on company registration documents:
									<ul>
										<li>Any changes regarding to information on company registration documents must be reported to Hong Kong government by separate procedures other than annual renewal process. Government and services fee applies.</li>
									</ul>
								</li>
							</ul>
							<h3>Step 2 : Tax filing, Accounting and Audit</h3>
							<ul>
								<li>Audit and tax filing are statutory requirement of every company on annual basis</li>
								<li>Companies are recommended to pick either “31-December” or “31-March” as the closing date of financial reporting as same as the cut-off date of Hong Kong Government.</li>
								<li>Maintain proper accounting books and records to produce financial statements for tax return filing for 7 years.</li>
								<li>Tax filing involves:
									<ul>
										<li>Employer’s Return</li>
										<li>Profits Tax Return&nbsp;(16.5% since 2008/09 and onwards)</li>
									</ul>
								</li>
								<li>Audit service must rendered by independent auditor (Certified Public Accountant – CPA).
									<ul>
										<li>Service fee of audit should be quoted separately according to certain criteria e.g. business size, annual turnover, amount of time spent on the book,&nbsp;and etc.</li>
									</ul>
								</li>
							</ul>
