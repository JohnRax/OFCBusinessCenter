									<span aria-hidden="true" class="avia_message_box_icon" data-av_icon="" data-av_iconfont="entypo-fontello"></span>
									<p>To setup a HK Limited Company, the company’s members is not required to be present in Hong Kong.</p>
									<p>Company’s owners are not required to be interviewed with any authority in Hong Kong.</p>
								</div>
