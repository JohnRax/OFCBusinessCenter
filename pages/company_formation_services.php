							<h3><span style="line-height: 1.5;">Step 1 : Renewal of Company Registration Documents</span></h3>
							<ul>
								<li>For maintaining government’s record of your company, government charges annual renewal fee and required to be settled within 42 days after anniversary date of your company.</li>
								<li>All the information provided for incorporation must be maintained and be valid, these include the information as follows:
									<ul>
										<li>Hong Kong Registered Office Address</li>
										<li>Company Secretary</li>
										<li>Director(s)</li>
										<li>Shareholder(s)</li>
