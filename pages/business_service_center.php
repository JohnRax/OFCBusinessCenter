						<h2>What is Registered Office Address?</h2>
						<p>According to <a href="http://www.legislation.gov.hk/blis_pdf.nsf/6799165D2FEE3FA94825755E0033E532/707C1C4DC6BDF92848257A5500549A21/$FILE/CAP_622_e_b5.pdf" target="_blank">Hong Kong Companies Ordinance (Chapter 622 Section 658)</a>, every Hong Kong registered company must maintain&nbsp;<strong>a Hong Kong registered office address</strong> for statutory records of the company and receipt of acknowledging government’s correspondence.</p>
						<hr>
