							</div><a class="mwm-aal-item" id="keeping-payroll-records" name="keeping-payroll-records"></a>
							<h2>Keeping payroll records</h2>
							<p>Tax obligations as an employer commence when you hire the first employee no matter the employee work on the&nbsp;premises within Hong Kong or not</p>
							<p>On hiring the employee, the employer has&nbsp;to maintain a record of that employed person’s:</p>
							<ol>
								<li>personal particulars (name, address, identity card or passport number with country of issue, marital status)</li>
								<li>nature of employment (full time or part-time)</li>
								<li>capacity in which employed (e.g. sales manager, salesman, worker, in-house lawyer, accountant, director)</li>
								<li>amount of cash remuneration (regardless of denomination in domestic or foreign currency and remuneration paid overseas)</li>
								<li>non-cash and fringe benefits (such as quarters, holiday journey benefits, share award, share option)</li>
								<li>employer’s and employee’s contributions to the Mandatory Provident Fund (MPF) or its equivalent</li>
								<li>employment contract and amendments to terms of employment</li>
								<li>period of employment</li>
							</ol>
							<p>During the employment period, an employer has to inform the Inland Revenue Department (“IRD”) for any change as follows:</p>
							<ol>
								<li>the employee’s personal particulars (such as change in residential and postal address, change in marital status)</li>
								<li>the employee’s terms of employment (such as a change from full time to part-time)</li>
								<li>the Hong Kong Identity Card No. of the employee</li>
							</ol>
							<p>Even the employment is end, the employer&nbsp;is required to keep business accounting records, including payroll records, for at least 7 years.</p><a class="mwm-aal-item" id="reporting-remuneration-paid-to-an-employee" name="reporting-remuneration-paid-to-an-employee"></a>
							<h2>Reporting remuneration paid to an employee</h2>
							<h3>General</h3>
							<p>When a business owner&nbsp;received an Employer’s Return from IRD, it must be completed and returned to IRD within 1 month even under the circumstances:</p>
							<ul>
								<li>none of&nbsp;employee (a Nil return should be made)</li>
								<li>the business has not commenced, or</li>
								<li>the business has ceased.</li>
							</ul>
							<h3>The Hong Kong Identity Card No. of the employee</h3>
							<p>If an&nbsp;employee does not possess a Hong Kong Identity Card (e.g. the employee was recruited from overseas or seconded from an overseas corporation) when the employer completes the Employer’s Return, filling in this employees&nbsp;passport number with country of issue is required. After filing the Employer’s Return, the employer still&nbsp;have to follow up with the employee and write in to inform the IRD when the employee’s Hong Kong Identity Card No. is known.</p>
							<h3>Continuous Employment</h3>
							<p>The employer has to report all the employees of following&nbsp;on Employer’s Return:</p>
							<ul>
								<li>single and paid an annual income of $120,000 or more</li>
								<li>married (regardless of amount)</li>
								<li>a part-time staff (regardless of amount)</li>
								<li>a director (regardless of amount)</li>
							</ul>
							<h3>New employment</h3>
							<p>When an&nbsp;new employment with&nbsp;Salaries Tax chargeable employee&nbsp;commence, the employer&nbsp;within 3 months of employing a person if you anticipate that the employee is likely to be chargeable to Salaries Tax.</p>
							<h3>On termination of his service (or death)</h3>
							<p>The employer have to file to IRD&nbsp;one month before the date of termination of his employment.</p>
							<h3>On his leaving Hong Kong for a substantial period of time</h3>
							<ul>
								<li>Expected date of departure of the employee should be obtained.</li>
								<li>File to IRD one month before the expected date of departure.</li>
								<li>From the date of filing to IRD&nbsp;to&nbsp;the time the employee’s tax clearance and “letter of release” by IRD having been made, the employer should not pay any amount including salaries, commission, bonus, reimbursement of rent/expense, money or money’s worth included to this employee.</li>
							</ul>
