							</div><a class="mwm-aal-item" id="what-is-the-requirement-of-owning-a-shelf-company" name="what-is-the-requirement-of-owning-a-shelf-company"></a>
							<h2>What is the requirement of owning a shelf company?</h2>
							<p>The obligation of a shelf Hong Kong company is completely the same as the Hong Kong company which is incorporated.</p>
							<p>Here is the &nbsp;list of minimum requirement of owning any Hong Kong private limited company:</p>
							<h3>Company Name:</h3>
							<p style="padding-left: 30px;">The company name, which can be in English or Chinese only or both, can not be the same or too similar as one already registered with Companies Registry (CR) in Hong Kong.</p>
							<p style="padding-left: 30px;">As the name&nbsp;“ready-made” suggests, the company you purchase from the incorporation intermediary&nbsp;is already registered with a name created.</p>
							<p style="padding-left: 30px;">Company name change is available after incorporation.</p>
							<h3>Members:</h3>
							<p style="padding-left: 30px;">Each company incorporated in Hong Kong must have at least one shareholder, one director and a company secretary. These position can either a natural person or body corporate regardless of their nationality/place of incorporation. For natural person directors and shareholders, they must be at least 18 years old.</p>
							<p style="padding-left: 30px;">A person can act in dual capacity, i.e. one can act as shareholder, director and secretary at the same time.</p>
							<p style="padding-left: 30px;">However, if a person is a sole director, this person&nbsp;could not act as director and company secretary at the same time, and at least on the the director(s) must be a natural person.</p>
							<p style="padding-left: 30px;">As a result, shelf Hong Kong companies can be sold to and owned by&nbsp;any&nbsp;adult. Copies of identity card or passport of all shareholder(s) and director(s) are required to be recorded in CR.</p>
							<h3>Company Secretary:</h3>
							<p style="padding-left: 30px;">Company Secretary must be a Hong Kong resident or a company incorporated or having a business address in Hong Kong.</p>
							<p style="padding-left: 30px;">For buyer of a shelf Hong Kong companies, your incorporation intermediary has acted this position before your purchase.</p>
							<h3>Registered Office Address:</h3>
							<p style="padding-left: 30px;">Each company must have one Hong Kong address to be used as Registered Office for communication with the Hong Kong Government. Post box address is not accepted.</p>
							<p style="padding-left: 30px;">The&nbsp;incorporation intermediary usually use their office address in the registration of the shelf Hong Kong companies.</p>
							<p style="padding-left: 30px;">Change of registered office address is available after incorporation.</p>
							<h3>Shareholding Structure:</h3>
							<p style="padding-left: 30px;">This could&nbsp;be the most complicated matter related to incorporation and ongoing maintenance of any private company in any jurisdiction.</p>
							<p style="padding-left: 30px;">In Hong Kong, there is no restriction on the amount of share capital and the share capital can be expressed in any currency other than HKD. Most of the companies are incorporated with a standard capital of HKD10,000 divided into 10,000 shares of HK$1.00 each unless otherwise advised.</p>
							<p style="padding-left: 30px;">For simplicity, all shelf or ready-made Hong Kong companies come with a capital of HK$10,000 divided into 10,000 shares of HK$1.00 each.</p>
							<p style="padding-left: 30px;">Increasing the share capital is available after incorporation.</p>
							<h3>Business Registration:</h3>
							<p style="padding-left: 30px;">Required by Hong Kong laws,&nbsp;a company once incorporated must apply for the Business Registration Certificate for doing business on submission of company&nbsp;incorporation application.</p>
							<p style="padding-left: 30px;">On the purchase of ready-made company, the application of change of company name and address will be handled the same time.</p>
