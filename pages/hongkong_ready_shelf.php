							<h2 style="text-align: left;">What does shelf / ready-made company mean?</h2>
							<p>In Hong Kong, some of the incorporation intermediaries pre-registered a large quantity of limited company&nbsp;at <a href="http://www.cr.gov.hk/" target="_blank">Companies Registry (CR)</a> but has never traded its record is completely clean. Those&nbsp;pre-registered Hong Kong companies are&nbsp;readily made by these intermediaries for reselling to others as if a piece of off-the-shelf hardware.</p>
							<p>Upon purchasing by others, the ownership of an off-the-shelf&nbsp;limited company&nbsp;will&nbsp;be transferred to new owners by filing amendment of director(s) and shareholder(s) to CR (change of company name, registered office address and company secretary are optional). Once the changes&nbsp;are filed in CR, the company is ready to use under the ownership of new owners.</p>
							<h3>Advantages:</h3>
							<ul>
								<li><strong>Speed:</strong> Skipping the lead time of CR in formation of &nbsp;a new Hong Kong limited company, buying a ready-made Hong Kong limited company can be done within 2 to 3 days since the process involves amendment of records only.</li>
								<li><strong>Age:</strong> When a freshly incorporated is not enough to make your customers and supplier feel&nbsp;confidence, buying an aged shelf-company is the feasible option.</li>
							</ul>
							<h3>Disadvantages:</h3>
							<ul>
								<li><strong>Cost:</strong> Price tag of a shelf company is definitely higher than the service package of new company incorporation because the intermediaries have to charge buyers for the cost of upkeep – the cost of annual renewals and maintenance before a ready-made&nbsp;company is sold.</li>
								<li><strong>Annual renewal date:</strong> Most likely, the anniversary date of incorporation of a shelf company is earlier than the date&nbsp;of purchase, so the new owner of the company should expect the due date of renewal is less than 1-year after the purchase.</li>
							</ul>
