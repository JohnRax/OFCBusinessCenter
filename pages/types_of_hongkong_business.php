						<section class="av_textblock_section" itemscope="itemscope" itemtype="https://schema.org/CreativeWork">
							<div class="avia_textblock" itemprop="text">
								<p>Incorporation of a company in Hong Kong is regulated by <a href="http://www.cr.gov.hk/en/home/index.htm" target="_blank">Companies Registry</a> under the law Companies.</p>
								<p>In Hong Kong, we can classify companies into to 2 types regarding to what the companies’ are limited by – <strong>Company Limited by Guarantee</strong> and <strong>Company Limited by Share</strong>, over 99% of registered limited companies are limited by shares, while the remaining are limited by guarantee which is a specific form of business entities for doing non-profit and charity works.</p>
								<p>In the category of Limited Company by Shares, they are group in 2 types regarding to level of confidence to their financial situation – <strong>Private Limited Company</strong> and <strong>Public Limited Company</strong>. Private Limited Company does not required to disclose its financial statue to the public, and has restriction of its number of investors, so it is the most popular business entity for small-to-medium size business.</p>
								<p>Among the Public Limited Company, some of them are also list on Hong Kong Stock Exchange, these company is named <strong>Listed Company</strong>.</p>
								<p>Finally, a minority of Hong Kong company is registered as <strong>non-Hong Kong company</strong>, it’s a type of legal entity for a oversea/foreign company to setup and own a Hong Kong company without separated legal entity to its oversea/foreign owning company. <strong>The non-Hong Kong company is held unlimited liable by its owner</strong>.</p>
								<blockquote>
									<p>We usually called a “Company limited by Shares” as “Company Limited (Co. Ltd.)” and even “Limited (Ltd.)”in short-term because this type of company is the dominant in number and popularity.</p>
									<p>A company named as “Limited” may theoretically refer to any kind of “Limited Company” (i.e. by Shares or by Guarantee) . However, we always understood that “Limited” is referring to Limited Company by Shares because of it majority in Hong Kong, and this company may be either Private Company or Public Company. The company are not required to include the word “private” of “public” in their company names in Hong Kong.</p>
								</blockquote>
