							<h2 class="av-special-heading-tag" itemprop="headline">Representative Office / Liaison Office</h2>
							<div class="special-heading-border">
								<div class="special-heading-inner-border"></div>
							</div>
						</div>
						<section class="av_textblock_section" itemscope="itemscope" itemtype="https://schema.org/CreativeWork">
							<div class="avia_textblock" itemprop="text">
								<p>Representative Office is the common name but someone may refer this business vehicle as “Liaison Office”, we believe they are refer the same type of business vehicle. This “vehicle” can only accept oversea companies (the companies incorporated outside Hong Kong) to register for them to establish “a place of business” in Hong Kong as “an extension from its oversea owner to Hong Kong” and using the identical name as its oversea company. Since it is not a separate legal entity in Hong Kong from its oversea owner, so its registration is NOT handled by Companies Registry. Representative office / Liaison office registration is handled by only Business Registration Office under Inland Revenue Department.</p>
								<p>Unlike all business entities in Hong Kong, <strong>Representative Office is NOT allowed to engage in any activity obtaining money from any source other than its parent company, thus Representative Office is impossible to make profit in Hong Kong.</strong> Legally, representative office is not an incorporated body. The liability incurred during operation of Representative Office is unlimited liable to the oversea parent company. It is also required that <strong>a local registered address</strong> must be provided to fulfill the legal requirement. The business owner can hire staffs using the name of Representative Office.</p>
								<p>Every Representative office must have one local representative to represent the office for government’s record and serving as a local contact to this Representative Office. The business owner can appoint either one type of following to take this position (More than 1 representative can be appointed for a Representative Office in Hong Kong):</p>
								<ul>
									<li>a natural person resident in Hong Kong</li>
									<li>a firm of solicitor or a solicitor corporation in Hong Kong</li>
									<li>a firm of a corporate practice of certified public accountants (practicing)</li>
								</ul>
								<p>Here is a summary of Representative Office in Hong Kong.</p>
								<ul>
									<li>Representative Office cannot conclude activities intended to make profit because they cannot obtain other source of money except its parent company.</li>
									<li>Only oversea company is able to register a Representative Office.</li>
									<li>Representative Office must register its name the same as its owner (the oversea company)</li>
									<li>Representative office is not an incorporated body thus it is not classified as company and need not fulfilling the compliance requirement same as company, although its registration is processed by companies registry.</li>
									<li>Its composition requires one local representative, it is only seem as a contact person in view of government.</li>
									<li>A local registered address is required for its composition.</li>
									<li>Hiring staff by the own name of Representative Office is allowed for building local workforce.</li>
									<li>Although it is unable generate profit, it is still required to finish and submit the annual tax return.</li>
								</ul>
								<p>Here is a brief comparison between advantages and disadvantages of using Representative Office:</p>
