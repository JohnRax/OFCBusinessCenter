							<h2 class="av-special-heading-tag" itemprop="headline">Partnership</h2>
							<div class="special-heading-border">
								<div class="special-heading-inner-border"></div>
							</div>
						</div>
						<section class="av_textblock_section" itemscope="itemscope" itemtype="https://schema.org/CreativeWork">
							<div class="avia_textblock" itemprop="text">
								<blockquote>
									<p>Partnership can be divided into <strong>General Partnership</strong> and <strong>Limited Partnership</strong>. Unless further specified, we refer Partnership as General Partnership in this chapter. We will cover the comparison between them in this section.</p>
								</blockquote>
								<p>Partnership is a form of business entity which involve 2 to 20 owners. When a person joins with other people to doing business with common view of intention for profits, this is a Partnership firm defined by the <strong>Partnership Ordinance</strong> in Hong Kong. Therefore, when a person shares the profits of a business consisting of other owners because of his effort contributed to this business, this person is in the nature of running business and in a form of Partnership with other owners.</p>
								<p>This form of business must apply for a Business Registration Certificate within 1 month when the business begins to operate. The Partnership Ordinance regulates the rights and obligations of partners in Partnership.</p>
								<p>The partnership firm’s structure, duties and rights of each partner must be stated on the Partnership Agreement and which is an mutual agreeing between all the partners.</p>
							</div>
						</section>
