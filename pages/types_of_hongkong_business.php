						<section class="av_textblock_section" itemscope="itemscope" itemtype="https://schema.org/CreativeWork">
							<div class="avia_textblock" itemprop="text">
								<p>In Hong Kong, Partnership Ordinance regulates the rights and obligations of partners in Partnership.</p>
								<p>Basically, it requires every partners in the Partnership has “fiduciary duty” (in other word, compulsory duty to behave trustworthy, honest, fair and reliable to the firm and to the other partners. Generally speaking, the partners must provide full and true information to their partners and the firm; the partners should disclose any benefit derived from any transaction concerning the Partnership firm (including the use of the firm name and its business connections); the partners must not to carry on any business for the same business nature to compete with the firm.</p>
