							<div class="avia_textblock" itemprop="text">
								<p>Hong Kong is one of the best cities for doing business, when foreign companies (which is those companies incorporated outside Hong Kong) are prepared to establish a place of doing business here, the investors can choose either setting up a Representative Office, incorporation of a Private Company limited by Shares (a.k.a Limited Company) and/or Non-Hong Kong Company under the Hong Kong Company Ordinance.</p>
								<blockquote>
									<h4>Do you know?</h4>
									<p>We sometimes hear the name “subsidiary” and “branch office” from foreign investors in Hong Kong. Although literally these two names mean a place of doing business which are an extension of their parent companies, they have different legal definition in Hong Kong:</p>
									<ul>
										<li>“Subsidiary” is NOT a legal business entity in Hong Kong, it is no way to set up a “subsidiary office” in the Hong Kong Companies Registry (CR);</li>
										<li>“Branch” (a.k.a Branch Office) has a specific meaning in Hong Kong: it is NOT a legal entity registered in CR, this is merely an extension of its parent business which must be incorporated / registered in Hong Kong. Branch office is not treated as a separate legal entity from its parent company, it does not has its own right so that the parent company is responsible for all the debts and liabilities of it.</li>
										<li>The registration of Branch is solely handled by Business Registration Officer (BRO) under Inland Revenue Department.</li>
										<li>For example, a local company which is a registered fashion retailing business opens a few chain stores sharing the same brand name and centrally managed by this company in Hong Kong. Each store must be registered as a “Branch” in BRO under the single parent company in order to doing business in each store.</li>
										<li>The advantage of registering each store as a branch over that of a company is that the parent company is responsible to handle all tax filing as well as annual return of all the branch thus lowering the accounting and auditing expense and enhancing central manageability.</li>
									</ul>
								</blockquote>
								<p>The “Non-Hong Kong Company” is a form of incorporated body exclusive to the oversea companies (incorporated outside Hong Kong) to <strong>solely owning a company in Hong Kong for engaging in intended profit-making business without separate source of fund, name, as well as liability from its oversea company</strong>.</p>
								<p>Alike the private company in Hong Kong, it has the requirement on company secretary and registered address. However, the owner (a oversea company) must submit its published account books, Certificate of Incorporation and any other information required for annual return and tax return in its jurisdiction of incorporation to Hong Kong Companies Registry on the registration of a “non-Hong Kong Company”. After registration, these document are required for submitting annual return and tax return.</p>
