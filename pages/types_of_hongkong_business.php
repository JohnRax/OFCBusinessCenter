						<section class="av_textblock_section" itemscope="itemscope" itemtype="https://schema.org/CreativeWork">
							<div class="avia_textblock" itemprop="text">
								<p>In Hong Kong Partnership Ordinance, the Partnership Agreement is an mutual agreeing between partners in a Partnership to state and control their rights, obligations and relationship to outsider of the partners in a Partnership business. The agreement can be either in form of written document or verbal agreement. If particular matters are not express in the Partnership Agreement, Partner Ordinance implies into the partners’ relationship to prescribe the solution of arguments and avoid future dispute.</p>
								<p>The Partnership Agreement provides terms on the Partnership and partners’ relationship, it usually is covering how to conduct the business, to control finance of the business, and to terminate the business.</p>
							</div>
						</section>
