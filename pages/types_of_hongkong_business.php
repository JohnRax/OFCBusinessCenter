								<p>Speaking of owners’ liability of Sole Proprietorship and Partnership, we can easily understand the owner(s) of these business entities are people – a natural born people – and their firms are binded to them personally. Legally, the firm and the owner are not the separate legal body thus the owners are liable to unlimited personal liability to their firm.</p>
								<p>While a natural person obtains it personhood when he is born, a company does so when it is incorporated according to certain company laws (i.e. Company Ordinance in Hong Kong). Precisely speaking, a company is a fictitious person who is treated by law as if they were a natural person, we call this artificial form of legal entity as <strong>Legal Person</strong>.</p>
								<p>Unlike other business form, the major difference between Limited Company) &nbsp;and Sole Proprietorship or Partnership is <strong>how the ownership of a company is distributed among owners</strong>:</p>
								<blockquote>
									<p>People or organization can acquire the ownership of a company by acquiring “shares” which is issued by this company and holding the “shares” to express his owner’s rights and duties in the company.</p>
								</blockquote>
								<p>The owners of “shares” is named as “Shareholders”, and the action of holding the shares is “shareholding”. “Shareholders” is the owner of a company because of injection of capital. A shareholder can precisely expresses his portion of ownership in a company – degree of ownership is proportional to his&nbsp;ratio&nbsp;of shareholding&nbsp;among all of the shareholders. Since a company is a separate legal person to the shareholders, the loss incurred by the company is limited to the Shareholders’ investment and do not liable to them personally. As returns, Shareholders can shares the company’s profit by receiving “dividend” per shares.</p>
								<p>In the contrast, Company has legal personality, it means a company is capable of having legals rights and duties within a legal system, for example, a company is able to enter into contracts, sue and be sued. The law treats a company as a natural person. &nbsp;Therefore, position of shareholder of a company can be acted by either natural person or legal person, the investors can diversify their risk by simply acquiring shares of a company which is shareholding a variety of companies.</p>
								<blockquote>
									<p>To conclude, Limited Company is a man-made entity to&nbsp;encourages&nbsp;the investors to joint their effort together and own an separate legal entity for venturing in the aim of profits; while their liability to loss are limited to their investment and profit are shared in proportion to each investor’s investment.</p>
								</blockquote>
