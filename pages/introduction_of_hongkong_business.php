							<div class="avia_textblock" itemprop="text">
								<p>In fact, you&nbsp;are not required to describe your company’s business nature to the government during business registration, and you are not bounded to perform this kind of business after incorporation. Of course some business are restricted, mostly the business has relation to public interest, apart from them, most of the trading and consultation services need not extra permission.</p>
								<p>&nbsp;</p>
