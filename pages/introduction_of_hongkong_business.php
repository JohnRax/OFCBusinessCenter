						<div class="av-special-heading av-special-heading-h3 avia-builder-el-7 el_after_av_textblock el_before_av_textblock" style="padding-bottom:10px;">
							<h3 class="av-special-heading-tag" itemprop="headline">Simple and Low Tax Regime</h3>
							<div class="special-heading-border">
								<div class="special-heading-inner-border"></div>
							</div>
						</div>
						<section class="av_textblock_section" itemscope="itemscope" itemtype="https://schema.org/CreativeWork">
							<div class="avia_textblock" itemprop="text">
								<p>Offering a predictable tax system and low tax rate, Hong Kong is one of the pro-business city. This city imposes only 3 kind of direct taxes – <strong>Profits Tax</strong> (16.5% for incorporated body), <strong>Salaries Tax</strong> (max 15% for personal income) and <strong>Property Tax</strong> (to property owner). Most of the taxes that are common around the world that are not the case in Hong Kong.</p>
								<p>Neither sales tax or VAT, withholding tax, capital gains tax, dividends tax or estate tax are found here.</p>
							</div>
						</section>
