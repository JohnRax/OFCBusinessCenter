							<div class="avia_textblock" itemprop="text">
								<p>In 1997, the sovereignty of Hong Kong was transferred from United Kingdom to People’s Republic of China but Hong Kong has a separate political and legal system from Chinese sovereignty. According to <a href="http://www.basiclaw.gov.hk/en/index.html" target="_blank">the Basic Law</a>, Hong Kong has retained all the fundamental strengths such as independent judiciary, the rule of law and free flow of information and capital that fortifies our success as a highly efficient business city and enhances its attractiveness of access to opportunities in Mainland China.</p>
