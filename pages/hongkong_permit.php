							<h3>Application of VISA&nbsp;via local sponsor</h3>
							<p>In case your visit requires a VISA, apart from proceeding to the&nbsp;nearest Chinese diplomatic and consular missions and apply for it from there, you may proceed to a local sponsor to shorten the processing time in some case.</p>
							<p>A local sponsor must be either a valid Hong Kong company&nbsp;with valid Business Registration or a Hong Kong resident with valid Hong Kong identity card.</p>
							<h3>Entry assessment to visitors</h3>
							<p>For visitors to Hong Kong applying visiting Visa or just landed in Hong Kong at the border, you are advised be prepared for questions from the officers regarding the following:</p>
							<ul>
								<li>Tourism / Social visitor
									<ul>
										<li>Return tickets to your home country</li>
										<li>Place of accommodation in Hong Kong</li>
										<li>A route or proposed route of your visit</li>
										<li>Companions of your visit</li>
										<li>Reasonably sufficient funds for staying in Hong Kong</li>
									</ul>
								</li>
								<li>Business visitor (in addition to above items):
									<ul>
										<li>Place of residence and office outside Hong Kong in your home country</li>
										<li>Your intended period of stay in Hong Kong</li>
										<li>Wages sourced from Hong Kong during your business visit to Hong Kong</li>
										<li>Financial proof to&nbsp;your stay in Hong Kong</li>
										<li>Business related planning about&nbsp;your stay in Hong Kong.</li>
									</ul>
								</li>
							</ul>
							<div class="av-special-heading av-special-heading-h2 avia-builder-el-2 el_after_av_heading el_before_av_heading" style="padding-bottom:10px;">
								<h2 class="av-special-heading-tag" itemprop="headline">Extension of Visitor VISA</h2>
								<div class="special-heading-border">
									<div class="special-heading-inner-border"></div>
								</div>
							</div>
							<p>Once the visitors has landed in Hong Kong with valid permit or VISA, the visitors can apply for the first extension before the expiry date of its existing permit.</p>
							<p>The application of first extension is usually not difficult to obtain approval when the visitor retain the statue of bona fide visitor to Hong Kong. The officer&nbsp;exterminate and may interview with&nbsp;applicant during the application.</p>
							<p>The visitor must prepare a written letter to justifying the sound reasons for staying in Hong Kong and be present to officer with those application form. The reasons stated should be sound like realistic with reasonable proof.</p>
							<p>Theoretically, unlimited number of extension can be granted by Hong Kong government to a visitor. In practices, most of the visitors can only get approved for their first extension. The application of further extension requires strong and solid supporting from third parties such as a local business body as well as the local person to show the importance of your present in Hong Kong is beneficial to Hong Kong society&nbsp;(e.g. to increase the tax income to Hong Kong government).&nbsp;The procedure of application to&nbsp;further extension is the same as that of first extension.</p>
							<h3>Refusal and Appeal</h3>
							<p>No appeal is available for all of the rejected VISA application.</p>
							<p>The visitor VISA and permit of a visitor can be removed by Hong Kong government without any notice at any moment.</p>
							<blockquote>
								<h4>Shuttle between Hong Kong</h4>
								<p>By the law, a visitor is able to&nbsp;make shuttle trip between&nbsp;Hong Kong and Macau (Macau SAR) or Shenzhen (the nearest city in Mainland China) where both regions are adjacent to Hong Kong and stay at these cities for a short&nbsp;period, and immediately re-enter hong Kong as visitor again for a new permit of stay in Hong Kong.</p>
								<p>However, the re-entering visitor may alert the immigration officers in Hong Kong since they may not pass the entry assessment and get rejected to entry.</p>
							</blockquote>
