							<p>Hong Kong government offers&nbsp;Travel Pass providing simplified immigration procedures for frequent business visitors&nbsp;who hold valid passports and are eligible to come to Hong Kong without a visa or entry permit for their&nbsp;visit.</p>
							<h3>Who can apply for?</h3>
							<p>The visitors must fulfill the circumstances as follows:</p>
							<ul>
								<li>with genuine business need to visit Hong Kong frequently, and</li>
								<li>previously visited Hong Kong on not less than&nbsp;3&nbsp;occasions (not include&nbsp;shuttle visiting to Mainland China or Macau) in the 12 months prior to the application, or</li>
								<li>your visits could&nbsp;bring substantial benefits to Hong Kong and the Immigration Department is satisfied with your proposal.</li>
							</ul>
							<p>Using Hong Kong residents’ counters and&nbsp;e-Channel service for simplified immigration clearance are&nbsp;the major&nbsp;benefit of Travel Pass holders procedures.</p>
