							<div class="av-special-heading av-special-heading-h1 avia-builder-el-0 el_before_av_heading avia-builder-el-first" style="padding-bottom:10px;">
								<h1 class="av-special-heading-tag" itemprop="headline">Visiting to Hong Kong is easy</h1>
								<div class="special-heading-border">
									<div class="special-heading-inner-border"></div>
								</div>
							</div>
							<p>The majority of visitors from about 170 countries and territories&nbsp;are granted a visa upon arrival at Hong Kong thus they may not&nbsp;need to apply it in&nbsp;advance.</p>
							<p>Hong Kong government, politically known as government of <a href="http://www.gov.hk/" target="_blank">the Hong Kong Special Administrative Region (HKSAR)</a>,&nbsp;welcome visitors worldwide and travelers are easy to be admitted as a visitor to Hong Kong. You will be informed the activity permitted under visitor status and applying for&nbsp;an extension to your visitor visa.</p>
							<p>For frequent visitors that are traveling to Hong Kong on the business purposes, &nbsp;they can apply for extra power to their visitor visa to gain speedy access to Hong Kong.</p>
							<div class="av-special-heading av-special-heading-h2 avia-builder-el-1 el_after_av_heading el_before_av_heading" style="padding-bottom:10px;">
								<h2 class="av-special-heading-tag" itemprop="headline">Visitor VISA</h2>
								<div class="special-heading-border">
									<div class="special-heading-inner-border"></div>
								</div>
							</div>
							<h3>Condition to stay as visitor</h3>
							<p>The visitor with&nbsp;visiting VISA / Entry Permit to HKSAR must not take part in the activities&nbsp;as&nbsp;follows:</p>
							<ul>
								<li>take up any employment in Hong Kong whether paid or unpaid;</li>
								<li>receive any work or employment related training;</li>
								<li>not become a student at a school, university or other educational institution;</li>
							</ul>
							<blockquote>
								<p>Visitors has&nbsp;obtain&nbsp;to Employment VISA prior to visiting Hong Kong for employment-related&nbsp;purposes and Study VISA after enrolled into local qualified educational institutions.</p>
							</blockquote>
							<p>For business-related visiting to Hong Kong, the holder of visiting VISA / Entry Permit can take part in the activities as follows:</p>
							<p>“Visitors” may generally be allow to the activities in Hong Kong as follows:</p>
							<ul>
								<li>business-related activities: concluding contracts or submitting tenders;</li>
								<li>examining or supervising the installation/packaging of goods or equipment;</li>
								<li>participating in exhibitions or trade fairs (except selling goods or supplying services direct to the general public – i.e. retailing is strictly prohibited), settling compensation or other civil proceedings;</li>
								<li>participating in product orientation; and</li>
								<li>attending short-term seminars or other business meetings.</li>
							</ul>
							<h3>Who can apply for?</h3>
							<p>Generally speaking, every visitor to Hong Kong must have a valid passport that will not expire not less than one&nbsp;month&nbsp;after the period of their intended stay in Hong Kong. For <a href="http://www.unhcr.org/pages/49c3646c158.html" target="_blank">people&nbsp;of statelessness</a>, the identity documents issued to stateless persons must not expire within not less than two months after their period of intended stay.</p>
							<p>As Hong Kong is well-known for its international free port for businesses, citizens of about 170 qualified&nbsp;countries do not even&nbsp;need a visa to access&nbsp;Hong Kong for leisure, social and business, they&nbsp;are allowed to stay for periods from 7 to 180 days depending on nationality. To check if your nationality is eligible for a Visa-free visiting to Hong Kong, you can contact the Chinese Consulates General in your country, their contact can be found in <a href="http://www.fmprc.gov.cn/mfa_eng/wjb_663304/zwjg_665342/2498_665360/" target="_blank" title="Chinese Consulates General around the world">here</a>. Further supporting information can be found on the <a href="http://www.immd.gov.hk/en/services/hk-visas/visit-transit/visit-visa-entry-permit.html" target="_blank" title="Visit Visa / Entry Permit Requirements for the Hong Kong Special Administrative Region">Hong Kong Immigration Department</a>. If your nationality is not a Visa-free country, please apply for Visa at your nearest&nbsp;<a href="http://www.fmprc.gov.cn/mfa_eng/wjb_663304/zwjg_665342/2498_665360/" target="_blank">Chinese diplomatic and consular mission</a>.</p>
							<p>The above information is valid only for “bona fide visitors” to Hong Kong.</p>
