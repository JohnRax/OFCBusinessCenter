						<h2>What is Work Visa in Hong Kong?</h2>
						<p>The name “Employment Visa” precisely define what “Work Visa” is. Employment Visa is a permit for its holder to entry and stay in Hong Kong to take up employment. It is a kind of “residential visa” so its holder is a Hong Kong residents and can apply for their family members to live in Hong Kong as Hong Kong residents. After 7 years of continuous residence in Hong Kong, the holder can apply for earning the statue as a Hong Kong Permanent Residence for benefits. Since the law of Hong Kong allows dual nationality, Hong Kong Permanent Resident can be the national of other countries at the same time.</p>
						<h3>Expats Living</h3>
						<p>Hong Kong is a Chinese-dominant community of considerable size of expatriates from all over the world.&nbsp; Expats are staying in Hong Kong enjoying a cosmopolitan lifestyle while they can always find suitable schools for their children or enjoy international cuisines, including those from their home countries.</p>
						<p>Hong Kong always welcome worldwide professions and talents to stay and bring benefits to the world-class city in Asia-Pacific.</p>
						<p>Expats are free to inquiries of their case to any <a href="http://www.fmprc.gov.cn/eng/default.htm" title="Chinese diplomatic and consular mission">Chinese diplomatic and consular mission</a> or the <a href="http://www.immd.gov.hk/eng/contactus/address.html" title="Immigration Department">Hong Kong SAR Government Immigration Department</a>.</p>
					
