						<div class="av-special-heading av-special-heading-h3 avia-builder-el-10 el_after_av_textblock el_before_av_textblock" style="padding-bottom:10px;">
							<h3 class="av-special-heading-tag" itemprop="headline">What is the composition of a Private Company Limited by Shares?</h3>
							<div class="special-heading-border">
								<div class="special-heading-inner-border"></div>
							</div>
						</div>
						<section class="av_textblock_section" itemscope="itemscope" itemtype="https://schema.org/CreativeWork">
							<div class="avia_textblock" itemprop="text">
								<p>Shareholders, Directors, and Company Secretary are the three required positions of a Limited Company, they are also named Founder Members, First Directors and First Company Secretary during the incorporation process.</p>
							</div>
						</section>
						<div class="avia-image-container avia_animated_image avia_animate_when_almost_visible pop-up av-styling- noHover av-overlay-on-hover avia-builder-el-12 el_after_av_textblock el_before_av_textblock avia-align-center avia_start_animation avia_start_delayed_animation" itemscope="itemscope" itemtype="https://schema.org/ImageObject">
							<div class="avia-image-container-inner">
							<div class="av-image-caption-overlay">
								<div class="av-caption-image-overlay-bg" style="opacity:0.4; background-color:#444444;"></div>
								<div class="av-image-caption-overlay-position">
									<div class="av-image-caption-overlay-center" style="color:#ffffff;">
										<p>Overview of composition of a Private Company Limited by Shares</p>
									</div>
								</div>
							</div><img alt="composition of Private Company Limited by Shares" class="avia_image" itemprop="thumbnailUrl" src="http://asiabc.co/wp-content/uploads/hong-kong-limited-company.png" title="hong-kong-limited-company"></div>
						</div>
						<section class="av_textblock_section" itemscope="itemscope" itemtype="https://schema.org/CreativeWork">
							<div class="avia_textblock" itemprop="text">
								<ul>
									<li><strong>Directors.</strong> A minimum of one director and unlimited maximum number of directors are allowed. The director could be an individual or a company (body corporate). The individual director can be of any nationality and need not be resident in Hong Kong, while corporate director can be incorporated in any place. Directors must be at least 18 years of age and must not be bankrupt or convicted for any malpractices. There is no requirement for the directors to also be shareholders. Nominee directors can also be appointed.</li>
								</ul>
								<blockquote>
									<p>At least one of the director must be nature person.</p>
								</blockquote>
								<ul>
									<li><strong>Founder member(s).</strong>&nbsp;A kind of shareholders that initiate the the incorporation process of a company. The Company must have at least one shareholder and but could not have more than 50 shareholders (members). Alike Director, the individual shareholder must be at least 18 years of age, either an individual&nbsp;or a company (body corporate) could be. Neither&nbsp;residency and nationality (place of incorporation for body corporate shareholder) requirement for shareholders are imposed. Meanwhile, a director and shareholder can be the same or different person. &nbsp;100% local or foreign shareholding is allowed. Appointment of nominee shareholders is possible.</li>
								</ul>
								<blockquote>
									<p>A company can only be shareholding or directed by an individual or a body corporate.</p>
								</blockquote>
								<ul>
									<li><strong>Company Secretary.</strong>&nbsp;Each company must appoint at least a company secretary. The secretary, if an individual, must ordinarily reside in Hong Kong; or if a body corporate, must have its registered office address (a place of business) in Hong Kong. It has to be noted that in case of a sole director/shareholder, the same person cannot act as the company secretary. The company secretary is responsible for maintaining the statutory books and records of the company and must also ensure the company’s compliance with all statutory requirements. It is widely accepted that the founder members to employ a professional company secretary firm in Hong Kong to act as company secretary.</li>
									<li><strong>Registered Office Address.</strong>&nbsp;A local Hong Kong address as the registered address of the company must be provided to Company Registries, &nbsp;this address must be a physical address, and must not&nbsp;be a PO Box because the government letters will be sent to there for communication with the company. The intended purpose of this address is not limited, so that it can be a commercial, industrial, or residential address. The address must be able to be reached by local postal service because Annual Return and annual Tax Return are sent to this address via local postal service.&nbsp;It is commonly accepted that the company can hire local company secretary firm to provide a local address as well as mail handling services for a Hong Kong company.</li>
									<li><strong>Share Capital.</strong> There are two types of share capital – <strong>Authorized capital</strong> and <strong>Issued share&nbsp;capital</strong>. There is no minimum share capital requirement, the general practice in Hong Kong is to have an authorized capital of HK$ 10,000 represented by 10,000 ordinary shares (other type of shares are allowed according to the designated right of its holder) of HK$ 1.00 each. The authorized capital can be increased anytime after incorporation. The minimum issued share capital is usually 1 share of HK$ 1.00. There is no limit or restriction on the maximum amount of share capital for both types. Share capital can be expressed currencies other than Hong Kong Dollar. Shares can be freely transferred.</li>
									<li><strong>Articles of Association (M&amp;A).</strong> Certain documents must be prepared which will form the constitution of every company. The M&amp;A &nbsp;must be registered with the Companies Registry, we have extra details. It is always recommended to seek professional advice to create the M&amp;A of you company to fulfill your specific demand on the shareholding structure.</li>
								</ul>
								<blockquote>
									<p>Memorandum of Association is no longer required since the Articles of Association covers the information of previously included in&nbsp;Memorandum of Association</p>
								</blockquote>
							</div>
