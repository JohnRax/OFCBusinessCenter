							<h2 class="av-special-heading-tag" itemprop="headline">Private Company Limited by Shares</h2>
							<div class="special-heading-border">
								<div class="special-heading-inner-border"></div>
							</div>
						</div>
						<section class="av_textblock_section" itemscope="itemscope" itemtype="https://schema.org/CreativeWork">
							<div class="avia_textblock" itemprop="text">
								<p><span class="av_dropcap1">W</span>hen doing business in other countries, set up a local limited company (i.e.&nbsp;incorporation) is always one of the choice. In Hong Kong, Private Company Limited by Shares (i.e. Limited Company) is straight-forward to register.</p>
								<div class="wp-caption alignright" id="attachment_552" style="width: 130px">
									<img alt="Logo of Companies Registry (CR) and Inland Revenue Department (IRD) - Incorporation in Hong Kong." class="wp-image-552 size-full" height="200" src="//asiabc.com.hk/wp-content/uploads/cr-ird-incorporation-hkg.png" width="120">
									<p class="wp-caption-text">Logo of Companies Registry (CR) and Inland Revenue Department (IRD) – Incorporation in Hong Kong.</p>
								</div>
								<blockquote>
									<p>All type of Limited Company have separated legal entity from its owners.</p>
								</blockquote>
								<p>Although incorporation needs comparatively extra effort and expense in setup and maintenance, its owners can always find its unmatched benefits comparing to other business entities in Hong Kong. Incorporation is regulated by a Hong Kong government authority namely <a href="http://www.cr.gov.hk/en/home/index.htm" target="_blank">Companies Registry (CR)</a>, and its&nbsp;commencing of business requires the registration of &nbsp;business in <a href="http://www.ird.gov.hk/eng/tax/bre_add_hrs.htm" target="_blank">Business Registration Office (BRO)</a>, a division in <a href="http://www.ird.gov.hk/eng/abo/ord.htm" target="_blank">Inland Revenue Department (IRD)</a>.</p>
								<p>The launch of “<a href="http://www.ird.gov.hk/eng/pdf/one_stop.pdf" target="_blank">One-stop Company and Business Registration</a>” facilitate the incorporation of local companies, applicant can submit the incorporation document to CR and IRD will be noticed to process the Business Registration at the same time.</p>
								<p>Successful application of incorporation will be issued 2 document – <strong>Certificate of Incorporation</strong> by CR and <strong>Business Registration&nbsp;Certificate</strong> by IRD.</p>
								<blockquote>
									<p>For reference, two major types of company to choose and its official definition are as follows:</p>
									<ul>
										<li>Company Limited by Shares – the liability of members is limited by articles of association to the amount unpaid on the shares respectively held by them. It can be classified into Private Company Limited by Shares and Public Limited Company by Shares. Over 90% of incorporated bodies are Company Limited by Shares</li>
										<li>Company Limited by Guarantee – no share capital and the liability of members is limited by the articles of association to the amount that the members respectively undertake to contribute to the assets of the company in the event of its being wound up. Non-profit-making organisations are usually registered as companies limited by guarantee.</li>
									</ul>
								</blockquote>
								<p>You are welcome to know more&nbsp;about various business entities in Hong Kong.</p>
