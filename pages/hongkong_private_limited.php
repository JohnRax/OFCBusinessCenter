							<h3 class="av-special-heading-tag" itemprop="headline">Initial Shareholdings and its share capital</h3>
							<div class="special-heading-border">
								<div class="special-heading-inner-border"></div>
							</div>
						</div>
						<section class="av_textblock_section" itemscope="itemscope" itemtype="https://schema.org/CreativeWork">
							<div class="avia_textblock" itemprop="text">
								<p>Every company which is limited by shares must state its share capital and initial shareholding structure among its initial shareholders (we call the initial shareholders as Founder Members). To describe the structure of shareholdings, the founders has to “create” the total number of shares and the value of each share that are owned by a company initially, and “transfer” the specific portion of total shares to each founder member according to specific member’s degree of ownership. The shares are “created” and “transferred” because none of the real money transaction is involved. In other words, these action are done on the paper.</p>
								<p>In nutshell, the “created” Share Capital on paper is “Authorized Shares Capital” while the “transfered” Share Capital on paper is “Issued Capital”.</p>
								<blockquote>
									<p><strong>Information Required</strong></p>
									<p>To fulfill the requirement on incorporation in Hong Kong, the founder members must correctly state the following:</p>
									<ul>
										<li>Class of Shares (eg. Ordinary, Preference, etc)</li>
										<li>Total number of shares proposed to be issued</li>
										<li>Currency</li>
										<li>Total amount of share capital to be subscribed by all of the founder members</li>
										<li>Total amount to be Paid Up or to be Regarded as Paid Up on the Shares Proposed to be Issued</li>
										<li>Total amount to Remain Unpaid or to be Regarded as Unpaid on the Shares Proposed to be Issued</li>
									</ul>
								</blockquote>
								<p>The company can issued extra shares after its incorporation for changing of shareholding structure, so it is not necessary to hold extra authorized share during capital incorporation.</p>
								<p>If the company issues <strong>more than one class of shares</strong> on incorporation, the rights of each class MUST be clearly stated on Particulars of Rights Attached to Shares, including whether shares are redeemable, voting rights, rights to receive dividends, rights to receive capital, etc.</p>
