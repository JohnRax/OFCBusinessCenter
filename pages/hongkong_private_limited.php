								<blockquote>
									<p>Memorandum of Association (abbreviation: MA) is no longer required for incorporation. Previously, the companies were required to prepare a Memorandum &amp; Articles of Association (abbreviation: M&amp;AA), right now only a copy Articles of Association is required.</p>
								</blockquote>
								<p>A company’s Articles of Association (abbreviation: AA) is a document which form the company’s constitution. It states the company’s initial shareholdings structure among founder members (initial shareholders), and defines the responsibilities of the directors and controlling of shareholders to directors . It must state that the liability of its members is limited.</p>
								<p>The AA is not a specified form. To facilitate the incorporation process, Companies Registry has provided <a href="http://www.cr.gov.hk/en/companies_ordinance/docs/AA_Sample_A.pdf" target="_blank">samples of &nbsp;AA</a>.</p>
