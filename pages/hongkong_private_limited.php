						<div class="av-special-heading av-special-heading-h3 avia-builder-el-15 el_after_av_textblock el_before_av_textblock" style="padding-bottom:10px;">
							<h3 class="av-special-heading-tag" itemprop="headline">What document and information should I ready for incorporation?</h3>
							<div class="special-heading-border">
								<div class="special-heading-inner-border"></div>
							</div>
						</div>
						<section class="av_textblock_section" itemscope="itemscope" itemtype="https://schema.org/CreativeWork">
							<div class="avia_textblock" itemprop="text">
								<ul>
									<li>
										<strong>Company Name in Hong Kong.&nbsp;</strong>After you came up with a name for your company, you can perform the <a href="http://www.icris.cr.gov.hk/csci/" target="_blank">Company Name Search via Cyber Search Centre</a>&nbsp;in <a href="http://www.cr.gov.hk/" target="_blank">CR website</a>&nbsp;to check if this name has any conflict to any existing record. The Name Search service is free of charge, and do not have any limitation on the number of search. It’s important to name your company name properly in order to prevent your company from violation to the Company Ordinance or infringement of Trademark of others.
									</li>
								</ul>
								<blockquote>
									<p><strong>Basic Requirement for a valid Company Name:</strong></p>
									<ul>
										<li>A Hong Kong company may either have a English Name, a Traditional Chinese Name, or both.&nbsp;Combining English and Chinese Characters is not allowed.</li>
										<li>The word “Limited” and the characters “有限公司” must be the last word in the English and Chinese (if have) Company Name of a Limited company.</li>
										<li>The word “Limited” may be dispensed after CR approval. Usually, the limited company is incorporated for non-profit purpose such as religion, art, charity and science can apply for a license from CR to use company name without the word “Limited”. This license requires lodgment fee and license fee, so it is optional.</li>
									</ul>
								</blockquote>
								<blockquote>
									<p><strong>Tips of compositing a Company Name:</strong></p>
									<ul>
										<li>It commonly to find lots of company names which have its statue marked as “Dissolved” during a company name search, the dissolved company is legally ended, its name is available for use.</li>
										<li>You are advised to input English and Traditional Chinese company name for searching.</li>
										<li>To protect the public interest, CR will disapprove a company name that may constitute a criminal offense, or offensive to the public interest.</li>
										<li>CR will require special approval to the proposed company name before registration when the name is likely connected to the Hong Kong government and any of its authorities.</li>
										<li>The use of specific words and expression in company names is regulated by other laws, using them is prohibited unless certain approval, e.g. “Bank”, “Stock Exchange”, “Certified Public Accountant”/“CPA”, “trust”, “chamber of commerce”.</li>
										<li>Some of the common words are widely considered as “the same as”, here are some of the well-known examples:
											<ul>
												<li>The definite article, e.g. The Chris Smith Consulting Limited is the same as Chris Smith Consulting Limited.</li>
												<li>The short form, e.g. “Hong Kong”, “Hongkong” and “HK” are the same.</li>
												<li>The symbol, e.g, “&amp;” and “and” are the same.</li>
												<li>The combination by “-”, e.g. combine 2 words with or without “-” are the same, so “Multi-trade”, “Multitrade” and “Multi Trade” are the same.</li>
											</ul>
										</li>
										<li>Sometimes, CR will reject a proposed name because the name is “too like” of an existing live record.</li>
										<li>Regarding to the protection of intellectual property, you are advised to search your proposed name in the <a href="http://ipsearch.ipd.gov.hk/" target="_blank">Hong Kong Trademark Register</a> after the company name search to reduce the risk of infringement of trademarks.
										</li>
									</ul>
								</blockquote>
								<ul>
									<li><strong>Registered Office Address in Hong Kong.</strong>&nbsp;This address must be a valid postal address located in Hong Kong. This address will be record in CR for postal communication between the Limited Company and Government</li>
								</ul>
								<blockquote>
									<p>Basic requirement for a valid Office Address:</p>
									<ul>
										<li>The intended purpose of this address is not limited, so that it can be a commercial, industrial, or residential address. The address must be able to be reached by local postal service because government’s communication e.g Annual Return and annual Tax Return are sent to this address.</li>
									</ul>
								</blockquote>
								<ul>
									<li><strong>Company Secretary in Hong Kong.</strong> It is a legally required member to each private company limited by shares to have at least one Company Secretary to perform the regulatory duties of this company to ensure the company’s ongoing compliance to its statutory requirement. It includes management of company’s statutory material (e.g. Register of Shareholders and Directors), holding statutory meetings of directors and shareholders, handling the change of register related to the company’s statute such as change of directors, registered address, and preparing and completing the filing of Annual Return. You may refer to [renewal] for more details of Company Secretary duties.</li>
								</ul>
								<blockquote>
									<p>CS is NOT a position of personal assistance to the company’s director.</p>
								</blockquote>
								<blockquote>
									<p><strong>Basic Requirement:</strong></p>
									<p>Each company must have at least one Company Secretary.&nbsp;Either a natural person who is a Hong Kong resident or a corporate body which is incorporated in Hong Kong.&nbsp;However, it the company has only one director (so it much be a natural person) then this person cannot take the position of company secretary at the same time.</p>
								</blockquote>
								<blockquote>
									<p><strong>Document required:</strong></p>
									<ul>
										<li>For Natural person company secretary, Hong Kong I.D. card and residential proof of the Hong Kong residential natural person.</li>
										<li>For Corporate Body company secretary, Hong Kong Certificate of Incorporation of Hong Kong company and its registered address.</li>
									</ul>
								</blockquote>
								<ul>
									<li>
										<strong>Director.&nbsp;</strong>The position of Directors in a Limited Company is a management staff employed by this company. Although it is not the owner of the company, it is hired to be the “captain” of this business vehicle to control of its daily courses. Guidelines to the duties and responsibilities of directors in a company may vary from company to company. Generally, these derive from a company‘s constitution and different regulatory authorities from business to business. Since no business is totally the same, general principles of directors’ duties is written for reference, they are not the exhaustive statements of respective law, they are guideline to business owners to assess the behavior of a director’s behavior. The outline of directors’ duties can be found and download <a href="http://www.cr.gov.hk/en/companies_ordinance/docs/Guide_DirDuties-e.pdf" target="_blank">here&nbsp;</a>
									</li>
								</ul>
								<blockquote>
									<p><strong>Basic Requirement:</strong></p>
									<p>At least one director of a company must be a natural person, nationality of this person is not restricted for a Private Company;&nbsp;Either a company incorporated in Hong Kong or outside Hong Kong can act as corporate director of a company.</p>
								</blockquote>
								<blockquote>
									<p><strong>Document required:</strong></p>
									<ul>
										<li>For corporate director, Certificate of Incorporation in Hong Kong or outside Hong Kong, and its registered address proof</li>
										<li>For natural person director, Hong Kong ID card or Passport copy (for non-Hong Kong resident), and his residential address proof</li>
									</ul>
								</blockquote>
								<p>Directors (both natural person or body corporate) have to sign to consent to act as director of the company. They can either sign to consent during incorporation or no later than 15 days after incorporation. If a founder member is also a director, then he must sign it on incorporation.</p>
							</div>
